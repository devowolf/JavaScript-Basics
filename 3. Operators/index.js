/*
*   Operators in JavaScript
*/

/*
* Arithematic Operators
*/

var firstNum = 25;
var secondNum = 5;

console.log("25 + 5: ", firstNum + secondNum); // Addition
console.log("25 - 5: ", firstNum - secondNum); // Subtraction
console.log("25 * 5: ", firstNum * secondNum); // Multiplication
console.log("25 / 5: ", firstNum / secondNum); // Division
console.log("25 % 5: ", firstNum % secondNum); // Modulus

/*
* Logical Operators
*/
console.log("\nLogical Operators:\n\n"); // Separate and add some spacing

console.log("25 > 5: ", firstNum > secondNum);
console.log("25 < 5: ", firstNum < secondNum);
console.log("25 == 5: ", firstNum == secondNum);
console.log("25 != 5: ", firstNum != secondNum);

/*
* typeof Operator
*/

console.log("\ntypeof Operator:\n\n"); // Separate and add some spacing
console.log('typeof firstNum: ', typeof firstNum);
console.log('typeof "hello": ', typeof "hello");